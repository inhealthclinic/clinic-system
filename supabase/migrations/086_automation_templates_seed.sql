-- Сидим системные шаблоны для автоматизаций воронки «Лиды»:
--   • touch_1 / touch_2 / touch_3      — три «касания» в этапе Касание
--   • work_task_24h / work_task_48h    — тексты задач для менеджера в этапе
--                                        В работе при отсутствии ответа
--   • touch_no_reply_task              — задача после 3-го касания без ответа
--
-- Тексты — заглушки '[ЗАПОЛНИТЬ ...]'. /lib/automation/sender.ts блокирует
-- отправку шаблонов с такими маркерами в WhatsApp, чтобы клиенту не уехало
-- '[ЗАПОЛНИТЬ ТЕКСТ КАСАНИЯ 1]'. Менеджер заходит в /settings/message-templates
-- (или /settings/automation) и пишет реальные тексты.
-- ─────────────────────────────────────────────────────────────────────────────

WITH seed(key, title, body, sort_order) AS (
  VALUES
    ('touch_1',             'Автоматизация: 1 касание',          '[ЗАПОЛНИТЬ ТЕКСТ КАСАНИЯ 1]', 110),
    ('touch_2',             'Автоматизация: 2 касание',          '[ЗАПОЛНИТЬ ТЕКСТ КАСАНИЯ 2]', 111),
    ('touch_3',             'Автоматизация: 3 касание',          '[ЗАПОЛНИТЬ ТЕКСТ КАСАНИЯ 3]', 112),
    ('work_task_24h',       'Автоматизация: задача через 24ч',   'Клиент не ответил уже 24 часа — свяжитесь повторно.', 113),
    ('work_task_48h',       'Автоматизация: задача через 48ч',   'Клиент не ответил уже 48 часов — финальное касание.', 114),
    ('touch_no_reply_task', 'Автоматизация: задача после 3-го касания без ответа',
                                                                 'Клиент не ответил после трёх касаний — решите по сделке.', 115)
)
INSERT INTO message_templates (clinic_id, title, body, key, sort_order, is_active)
SELECT c.id, s.title, s.body, s.key, s.sort_order, true
  FROM clinics c CROSS JOIN seed s
 WHERE NOT EXISTS (
   SELECT 1 FROM message_templates t
    WHERE t.clinic_id = c.id AND t.key = s.key
 );
